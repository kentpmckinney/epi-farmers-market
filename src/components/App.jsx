function App() {

  var d = new Date();
  var weekday = new Array(7);
  weekday[0] = "Sunday";
  weekday[1] = "Monday";
  weekday[2] = "Tuesday";
  weekday[3] = "Wednesday";
  weekday[4] = "Thursday";
  weekday[5] = "Friday";
  weekday[6] = "Saturday";

  var m = new Array();
  m[0] = "January";
  m[1] = "February";
  m[2] = "March";
  m[3] = "April";
  m[4] = "May";
  m[5] = "June";
  m[6] = "July";
  m[7] = "August";
  m[8] = "September";
  m[9] = "October";
  m[10] = "November";
  m[11] = "December";

  const date = d.getDate();
  const year = d.getFullYear();
  const month = m[d.getMonth()];
  const day = weekday[d.getDay()];
  let location = ''
  let hours = ''
  let booth = ''

  marketSchedule.forEach(function(item){
    if (item.day == day) {
      location = item.location
      hours = item.hours
      booth = item.booth
    }
  })

  let produceList = ''
  availableProduce.forEach(function(item){
    if (item.month == month) {
      produceList = item.selection.join(', ');
    }
  })

  return (
    <div className = 'container'>
      <h1>Avery Organics</h1>
      <br/>
      <InfoDisplay
        label={`Today's Market Location (${day}, ${date} ${month}, ${year})`}
        info={<div><span><b>Hours:</b> {hours}</span><br/><span><b>Location:</b> {location}</span><br/><span><b>Booth:</b> {booth}</span></div>}
      />
      <br/>
      <InfoDisplay
        label={`Today's Produce`}
        info={produceList}
      />
      <br/>
      <InfoDisplay>
        <MonthDisplay/>
      </InfoDisplay>
    </div>
  );
}

export default App;
